#hash bentuk pertama
hash = {'satu' => 1, 'dua' => 2, 3 => "selamat"} 
#dimana (key) => (value)
puts hash[3]

#hash bentuk kedua menggunakan simbol
hash = {:satu => 'selamat', :kedua => 3, :tigadeh => "urut"}
puts hash[:kedua]

#hash bentuk ketiga dan paling populer
hash = {satu: 'hello world', duaaja: 45, empat: "jangan takut"}
puts hash[:empat]